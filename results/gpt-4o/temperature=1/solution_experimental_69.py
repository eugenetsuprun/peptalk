def letterCombinations(digits: str) -> list[str]:
    if not digits: return []
    d = {'2': 'abc', '3': 'def', '4': 'ghi', '5': 'jkl', '6': 'mno', '7': 'pqrs', '8': 'tuv', '9': 'wxyz'}
    res = ['']
    for digit in digits:
        if digit in d:
            res = [prev + letter for prev in res for letter in d[digit]]
    return res
