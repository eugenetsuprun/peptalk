def letterCombinations(digits: str) -> list[str]:
    if not digits:
        return []
    phone = {
        '2': 'abc', '3': 'def', '4': 'ghi', '5': 'jkl',
        '6': 'mno', '7': 'pqrs', '8': 'tuv', '9': 'wxyz'
    }
    result = ['']
    for digit in digits:
        if digit in phone:
            result = [prefix + letter for prefix in result for letter in phone[digit]]
    return result